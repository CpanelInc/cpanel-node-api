/**
 * Common http verbs
 */
export enum HttpVerb {
    /**
     * Get request
     */
    GET,

    /**
     * Head request
     */
    HEAD,

    /**
     * Post request
     */
    POST,

    /**
     * Put request
     */
    PUT,

    /**
     * Delete request
     */
    DELETE,

    /**
     * Connect request
     */
    CONNECT,

    /**
     * Options request
     */
    OPTIONS,

    /**
     * Trace request
     */
    TRACE,

    /**
     * Patch request
     */
    PATCH,
}
